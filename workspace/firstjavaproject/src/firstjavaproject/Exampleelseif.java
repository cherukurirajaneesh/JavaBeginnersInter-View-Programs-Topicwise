package firstjavaproject;

public class Exampleelseif {

	public static void main(String[] args) {
		int x = 10 ;
		int y = 10 ;
		
		if (x == y){
			System.out.println("x is equal to y");
		}else if (x < y){
		System.out.println("x is less then y");
		}else {
		System.out.println("x is greater than y");
		}
	}

}